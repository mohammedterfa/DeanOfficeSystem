    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/samproject","root","");
            
            String sql = "update message set message_number=?, letter_attachments=?, type=?, subject=?, destination=?, is_urgent=?, send_method=? where id=?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(8, Integer.parseInt(id.getText()));
            pstmt.setString(1, message_number.getText());
            pstmt.setString(2, attachments.getText());
            String type="";
            if(in.isSelected()){
                type = in.getText();
            }
            if(ex.isSelected()){
                type = ex.getText();
            }
            pstmt.setString(3, type);
            pstmt.setString(4, subject.getText());
            String desi;
            desi = des.getSelectedItem().toString();
            pstmt.setString(5, desi);
            
            String ug="";
            if(jRadioButton1.isSelected()){
                ug = jRadioButton1.getText();
            }
            if(jRadioButton2.isSelected()){
                ug = jRadioButton2.getText();
            }
            pstmt.setString(6,ug);
            
            String sending_method="";
            if(through_circus.isSelected()){
                sending_method = through_circus.getText();
            }
            if(through_person.isSelected()){
                sending_method = through_person.getText();
            }
            
            pstmt.setString(7,sending_method );
            String stat="";
            if(jCheckBox1.isSelected()){
                stat = jCheckBox1.getText();
            }
            
            
            pstmt.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable_Display_Message.getModel();
            model.setRowCount(0);
            show_message();
            JOptionPane.showMessageDialog(null, "تم تعديل البيانات");
            conn.close();
            
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

