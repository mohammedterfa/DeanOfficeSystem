    public ArrayList<message> messagelist(){
        ArrayList<message> messagelist = new ArrayList<>();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/samproject","root","");
            
            String sql = "select * from message where source='"+side+"'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            message mes;
            
            while(rs.next()){
                mes = new message(rs.getInt("id"),rs.getString("message_number"),rs.getString("date"),rs.getString("letter_attachments"),rs.getString("type"),rs.getString("subject"),rs.getString("source"),rs.getString("destination"),rs.getString("is_urgent"),rs.getString("send_method"),rs.getString("is_sent"),rs.getString("sender"));
                messagelist.add(mes);
            }
            
        conn.close();
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
        
    return messagelist;
    
    }
    
    public void show_message(){
        ArrayList<message> list = messagelist();
        DefaultTableModel model = (DefaultTableModel)jTable_Display_Message.getModel();
        Object[] row = new Object[11];
        for(int i = 0;i<list.size();i++){
          row[0]=list.get(i).getmessage_sender();
          row[1]=list.get(i).getmessage_is_sent();
          row[2]=list.get(i).getmessage_send_method();
          row[3]=list.get(i).getmessage_is_urgent();
          row[4]=list.get(i).getmessage_destination();
          row[5]=list.get(i).getmessage_subject();
          row[6]=list.get(i).getmessage_type();
          row[7]=list.get(i).getmessage_letter_attachments();
          row[8]=list.get(i).getmessage_number_m();
          row[9]=list.get(i).getmessage_date();
          row[10]=list.get(i).getmessage_id();
          model.addRow(row);
        }
        
    }

